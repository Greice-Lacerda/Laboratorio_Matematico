# Passo 1: Definindo os valores (como as variáveis de uma equação)
nota1 = 8.5
nota2 = 7.0
nota3 = 9.0
nota4 = 6.5

# Passo 2: O cálculo da média (respeitando a precedência aritmética)
media = (nota1 + nota2 + nota3 + nota4) / 4

# Passo 3: Exibindo o resultado de forma elegante
print(f"\nA média das notas é: {media:.1f}\n")